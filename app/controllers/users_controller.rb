class UsersController < ApplicationController
    # ----------------------------------------- BEFORE ACTION --------------------------------------------
    before_action :locate_user, only: [:destroy]
    # --------------------------------------------------------------------------------------------
    # ----------------------------------------- INDEX --------------------------------------------
    def index
        @users = User.all
    end
    # --------------------------------------------------------------------------------------------
    # ----------------------------------------- NEW --------------------------------------------
    def new
        @user = User.new
    end
    # --------------------------------------------------------------------------------------------
    # ----------------------------------------- CREATE --------------------------------------------
    def create
        @user = User.new(user_params)
        if @user.save
            session[:user_id] = @user.id
            redirect_to root_path, notice: "Signed in successfully."
        else
            render :new, status: :unproccesable_entity
        end
    end
    # --------------------------------------------------------------------------------------------
    # ----------------------------------------- DESTROY --------------------------------------------
    def destroy
        session[:user_id] = nil
        @user.destroy
        redirect_to signin_path
    end
    # --------------------------------------------------------------------------------------------
    # ----------------------------------------- PRIVATE --------------------------------------------
    private 
        def locate_user
            @user = User.finda_by(params[:id])
        end

        def user_params
            params.require(:user).permit(:username, :password, :password_confirmation)
        end
    # --------------------------------------------------------------------------------------------
end
